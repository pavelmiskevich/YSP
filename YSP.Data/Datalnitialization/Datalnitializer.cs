        public static void RecreateDatabase(YSPDbContext context)
        {
            context.Database.EnsureDeleted();
            //context.Database.EnsureCreated();
            context.Database.Migrate();
        }

        public static void ClearData(YSPDbContext context)
        {
            ExecuteDeleteSql(context, "Categories");
            ExecuteDeleteSql(context, "Feedbacks");
            ExecuteDeleteSql(context, "Offers");
            ExecuteDeleteSql(context, "Positions");
            ExecuteDeleteSql(context, "Queries");
            ExecuteDeleteSql(context, "Regions");
            ExecuteDeleteSql(context, "Schedules");
            ExecuteDeleteSql(context, "Sites");
            ExecuteDeleteSql(context, "SystemStates");
            ExecuteDeleteSql(context, "Users");
            Resetldentity(context);
        }

        private static void ExecuteDeleteSql(YSPDbContext context, string tableName)
        {
            var rawSqlString = $"Delete from dbo.{tableName}";
            context.Database.ExecuteSqlRaw(rawSqlString);
        }

        private static void Resetldentity(YSPDbContext context)
        {
            var tables = new[] { "Categories", "Feedbacks", "Offers", "Positions", "Queries", "Schedules", "Sites", "SystemStates", "Users" }; //, "Regions"
            foreach (var itm in tables)
            {
                var rawSqlString = $"DBCC CHECKIDENT (\"dbo.{itm}\", RESEED, 1);";
                context.Database.ExecuteSqlRaw(rawSqlString);
            }
        }
